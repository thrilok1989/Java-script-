    # ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
    # üìä COMPREHENSIVE SUPPORT/RESISTANCE DISPLAY - Python Components
    # ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
    if merged_supports or merged_resistances:
        st.markdown("---")
        st.subheader("üìä SUPPORT & RESISTANCE ZONES")
        st.caption("Chart + OI Aligned | More ‚ñà = Stronger confluence")

        col_s, col_r = st.columns(2)

        with col_s:
            st.markdown("**üõ°Ô∏è SUPPORT ZONES**")
            if merged_supports:
                for i, s in enumerate(merged_supports[:3]):
                    strength_bars = "‚ñà" * min(s['strength'], 5)
                    sources = " + ".join(s['sources'])
                    zone_range = f"‚Çπ{s['range'][0]:,.0f} - ‚Çπ{s['range'][1]:,.0f}" if s['range'][0] != s['range'][1] else f"‚Çπ{s['price']:,.0f}"
                    st.success(f"S{i+1}: {zone_range} ({sources}) {strength_bars}")
            else:
                st.caption("No support levels detected")

        with col_r:
            st.markdown("**üß± RESISTANCE ZONES**")
            if merged_resistances:
                for i, r in enumerate(merged_resistances[:3]):
                    strength_bars = "‚ñà" * min(r['strength'], 5)
                    sources = " + ".join(r['sources'])
                    zone_range = f"‚Çπ{r['range'][0]:,.0f} - ‚Çπ{r['range'][1]:,.0f}" if r['range'][0] != r['range'][1] else f"‚Çπ{r['price']:,.0f}"
